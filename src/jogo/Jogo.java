    public static boolean posicoesIguais(Avatar avatar1, Avatar avatar2){
        int[] pos1= avatar1.getPosicao();
        int[] pos2= avatar2.getPosicao();
        if((pos1[0]==pos2[0])&&(pos1[1]==pos2[1])){
            return true;
        }
        return false;
    }
