     public static void instanciar() {
        floresta = new Floresta();
        frutas = new MatrizFrutas();
        avatar1 = new Avatar(1);
        avatar2 = new Avatar(2);
        if(Jogo.posicoesIguais(avatar1, avatar2)==true){
            avatar2.outraposicao();
        }
        Floresta.imprime();
        janela1 = new JanelaJogo();
    }
    

    
